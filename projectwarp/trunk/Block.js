function Block(x, y){
	
	var blk = new Sprite();
	blk.image = Textures.load("http://images3.wikia.nocookie.net/__cb20101211055706/nintendo/en/images/6/63/Brick_Block.png");
	this.addChild(blk);	
}

Block.prototype = new Sprite();
Block.prototype.width = 800;
Block.prototype.height = 200;



Block.prototype.draw = function(ctx){
	this.drawChildren(ctx);
}
